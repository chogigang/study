// return 문

//함수 생성
int sum(int n){
return n+10;


}



void main(){
//함수 호출  
print(sum(10)); 

}

/*
함수에서 return문은 값을 반환합니다.그리고 함수의 기능은 종료됩니다.
즉, 함수에서 return문이 실행 된 후의 코드는 실행이 되지 않습니다.
다음은 return문 아래에 print('pinkcoding'); 이라는 문구를 넣은 예제입니다.
함수는 return문을 만나면 기능을 종료하기 때문에 이 후의 코드는 실행되지 않습니다.
*/